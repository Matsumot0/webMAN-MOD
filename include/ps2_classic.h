static void enable_classic_ps2_mode(void)
{
	int fd;
	if(cellFsOpen((char*)PS2_CLASSIC_TOGGLER, CELL_FS_O_CREAT| CELL_FS_O_TRUNC |CELL_FS_O_WRONLY, &fd, NULL, 0) == CELL_FS_SUCCEEDED)
	{
		cellFsClose(fd);
		cellFsChmod((char*)PS2_CLASSIC_TOGGLER, MODE);
	}
}

